/**
 * Type in Js
 * Ther are 7 types in Js :
 * 1. Undefined 2. Null 3. String 4. Number 5. Boolean 6. Symbol 7. Objects
 */


// numbers
console.log(12);
console.log(3.25);
console.log(-45);
console.log(0);
console.log(-0);

// string
console.log("My name is loneWolf");
console.log('I will be in Google');
console.log(`I will live my life to the fullest`);

// boolean
console.log(true);
console.log(false);

// undefined and null
console.log(undefined);
console.log(null);

// object
console.log({"name": 'loneWolf'});

// symbol
console.log(Symbol('hello'))